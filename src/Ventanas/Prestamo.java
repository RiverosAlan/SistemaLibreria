    private void ListPresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListPresMouseClicked
    int colum=ListPres.getColumnModel().getColumnIndexAtX(evt.getX());
    int row=ListPres.getSelectedRow();
    
    if(colum==5)
    {
        
        String idp=ListPres.getValueAt(row, 0).toString();
        String idU=ListPres.getValueAt(row, 1).toString();
        String idL=ListPres.getValueAt(row, 2).toString();
        String feP=ListPres.getValueAt(row, 3).toString();
        String feD=ListPres.getValueAt(row, 4).toString();
        
        ArrayList <recibo> lista = new ArrayList<>();
        recibo listP=new recibo(idp,idU,idL,feP,feD);
        lista.add(listP);
        
        JasperReport reporte;
        try {
            reporte = (JasperReport)JRLoader.loadObject(nuevoPrestamo.class.getResource("/Ventanas/Recibo.jasper"));
        JasperPrint imprimir = JasperFillManager.fillReport(reporte,null,new JRBeanCollectionDataSource(lista));
            
            JasperViewer viewer = new JasperViewer(imprimir, false);
            viewer.show();
        } catch (JRException ex) {
            Logger.getLogger(nuevoPrestamo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    }//GEN-LAST:event_ListPresMouseClicked


