    private void deleteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMousePressed
        try {
            int idcell = ListPres.getSelectedRow();
            if(idcell <= -1){
                javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el usuario a borrar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                Statement stm = reg.createStatement();
                ResultSet counter = stm.executeQuery("SELECT * FROM `prestamo` WHERE `estado` = 'Deudor'");

                int count = 0;
                while(counter.next()){count++;}

                String list[][] = new String[count][7];
                int i = count;
                ResultSet re = stm.executeQuery("SELECT * FROM `prestamo` WHERE `estado` = 'Deudor'");
                while(re.next())
                {
                    i--;
                    list[i][0] = re.getString("idPrestamo");
                    list[i][1] = re.getString("CodUsuario");
                    list[i][2] = re.getString("CodLibro");
                    list[i][3] = re.getString("fechaPr");
                    list[i][4] = re.getString("fechaDe");
                    list[i][5] = re.getString("DiasP");
                    
                }
                String codl=list[idcell][0];
                int id =Integer.parseInt(list[idcell][0].substring(2,list[idcell][0].length()));
                if(id <= 0){
                    javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el usuario a borrar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    Statement stm2=null;
                    try {
                        stm2 = reg.createStatement();
                    } catch (SQLException ex) {
                        Logger.getLogger(Reports.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    try {
                        stm2.executeUpdate("UPDATE `prestamo` SET `estado` = 'No Deudor' WHERE `idPrestamo`='"+codl+"'");
                        javax.swing.JOptionPane.showMessageDialog(this, "¡Prestamo borrado! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        
                    } catch (SQLException ex) {
                        Logger.getLogger(Reports.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                MostrarDatosP();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteMousePressed

